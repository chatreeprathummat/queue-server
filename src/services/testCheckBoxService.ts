// ‡∏™‡∏£‡πâ‡∏≤‡∏á middleware ‡∏ó‡∏µ‡πà custom ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏≠‡∏±‡∏û‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå
export const upload = (req: Request, res: Response, next: Function) => {
    basicUpload.single('file')(req, res, (err: any) => {
        if (err) {
            return next(err);
        }
        
        if (req.file) {
            // ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å‡πÑ‡∏î‡πâ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å form data ‡πÅ‡∏•‡πâ‡∏ß
            const file = req.file;
            const { prNumber, documentCode, document_item_id } = req.body;
            
            // ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡πÉ‡∏´‡∏°‡πà
            const now = new Date();
            const dateStr = now.getFullYear().toString() +
                           (now.getMonth() + 1).toString().padStart(2, '0') +
                           now.getDate().toString().padStart(2, '0') +
                           now.getHours().toString().padStart(2, '0') +
                           now.getMinutes().toString().padStart(2, '0') +
                           now.getSeconds().toString().padStart(2, '0') +
                           now.getMilliseconds().toString().padStart(3, '0');
            
            const prNumberForFile = prNumber || 'PR-UNKNOWN';
            const documentCodeForFile = documentCode || 'DOC';
            
            const extension = path.extname(file.originalname);
            const newFilename = `${prNumberForFile}-${documentCodeForFile}-${dateStr}${extension}`;
            
            // ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå
            const oldPath = file.path;
            const newPath = path.join(path.dirname(oldPath), newFilename);
            
            try {
                fs.renameSync(oldPath, newPath);
                file.filename = newFilename;
                file.path = newPath;
                
                console.log(`üìÅ ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå: ${oldPath} -> ${newPath}`);
                console.log(`üìÑ ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•: PR=${prNumberForFile}, Code=${documentCodeForFile}`);
            } catch (renameError) {
                console.error('‚ùå ‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå:', renameError);
                // ‡∏ñ‡πâ‡∏≤‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ ‡πÉ‡∏´‡πâ‡πÉ‡∏ä‡πâ‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏î‡∏¥‡∏°
            }
        }
        
        next();
    });
};

